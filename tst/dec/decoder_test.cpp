namespace asn1 {
namespace ber {

void decoder_test()
{
	using Buffer = std::vector<byte>;

	const std::vector<Buffer> input =
	{
		{ 0x01, 0x01, 0x01 },
		{ 0x01, 0x01, 0xFF },
		{ 0x11, 0x08, 0x02, 0x01, 0x80, 0x09, 0x03, 0x80, 0xFB, 0x05 },
		{ 0x10, 0x08, 0x02, 0x01, 0x80, 0x09, 0x03, 0x80, 0xFB, 0x05 },
		{ 0x30, 0x13, 0x02, 0x01, 0x05, 0x16, 0x0e, 0x41, 0x6e, 0x79, 0x62, 0x6f, 0x64, 0x79, 0x20, 0x74, 0x68, 0x65, 0x72, 0x65, 0x3f },
		{ 0x01, 0x05, 0x0e, 0x83, 0xbb, 0xce, 0x2d, 0xf9, 0x3c, 0xa0, 0xe9, 0xa3, 0x2f, 0x2c, 0xaf, 0xc0 }
    };

    using tester = decoder::tester<>;

    const tester::test_item_array input_items =
	{
		{ // test1
			// input
			{ 0x01, 0x01, 0x01 },
			{ // expected
				{ { tag_t::boolean }, { 1 } }
			}
		},
		{ // test2
			// input
			{ 0x01, 0x01, 0xff },
			{ // expected
				{ { tag_t::boolean }, { 0xff } }
			}
		},
		{ // test3
			// input
			{ 0x11, 0x08, 0x02, 0x01, 0x80, 0x09, 0x03, 0x80, 0xFB, 0x05 },
			{ // expected
				{ { tag_t::set }, { 0x02, 0x01, 0x80, 0x09, 0x03, 0x80, 0xFB, 0x05 } }
			}
		},
	};

	ber::decoder::printer<> printer(std::cout);
	for (auto& i : input)
	{
		printer(i.data(), i.data() + i.size());
		printer.reset();
		std::cout << '\n';
	}

    tester t(input_items);
	t.run();
}

}}

int main()
{
	asn1::ber::decoder_test();
}

